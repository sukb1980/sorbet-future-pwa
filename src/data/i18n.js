export const formatPrice = (priceObj, currency) => {
  const currencyMap = { ZAR: 'R', BWP: 'P', MUR: 'Rs' };
  const symbol = currencyMap[currency] || 'R';
  const amount = priceObj?.[currency] ?? priceObj?.ZAR ?? 0;
  return `${symbol}${amount.toLocaleString()}`;
};
