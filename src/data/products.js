export const getProductById = (id) => products.find((p) => p.id === id) || null;
export const getProductsByCategory = (catId) => products.filter((p) => p.category === catId);
export const getProductsByBrand = (brand) => products.filter((p) => p.brand === brand);
export const getStockStatus = (productId, storeId) => {
  const product = getProductById(productId);
  if (!product) return 'out-of-stock';
  const qty = product.stockByStore[storeId] ?? 0;
  if (qty === 0) return 'out-of-stock';
  if (qty <= 3) return 'low-stock';
  return 'in-stock';
};
